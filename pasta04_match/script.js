//MATH
let num1 = 9.54578
let num2 = 5
console.log(Math.floor(num1)) //Arredondar um número pra cima;
console.log(Math.ceil(num1)) // Arredondar um número pra baixo;
console.log(Math.round(num1)) // Arredondar para o numero inteiro mias próximo;
console.log(Math.max(num1, num2)) // Achar o maior valor;
console.log(Math.min(num1, num2)) // Achar o menor valor;
console.log(Math.random()) // Gerar número aleatório entre 0 e 1 (o 1 não é incluído);
console.log(Math.random() * (10 - 5) + 5) // Gerar número aleatório entre 5 e 10 (max - mim) + min;